export function getCareerByName(name) {
  return careers.find(c => c.name.toLowerCase() === name.toLowerCase());
}

export function getCareersByCategory(category) {
  if (category === "All") return careers;
  return careers.filter(c => c.category === category);
}

export function searchCareers(query) {
  const q = query.toLowerCase();
  return careers.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.category.toLowerCase().includes(q) ||
    c.description.toLowerCase().includes(q) ||
    c.skills.some(s => s.toLowerCase().includes(q))
  );
}
